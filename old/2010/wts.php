			<h1><span class="numbers">01.</span> What's The Scene?: Rating for Transient Virtual Communities</h1>
			<div class="project_img">
				<img border="0" src="images/wts.jpg" alt="image of What's The Scene screens"/>
			</div>
			<h2>Problem Space</h2>
			<p class="text">
			Most events such as concerts, trade shows or amusements parks are temporary in nature. 
			These events often consist of  multiple simultaneously occurring sub-events where some 
			areas might be more appealing or interesting than others. The people that attend these 
			events have useful information to share about what’s going on, but often have no interest 
			in maintaining any further communication.
			</p>

			<h2>Solution</h2>
			<p class="text">
			What's the Scene? needed to be easy to use and intuitive, as well as allow users to quickly 
			access its content without investing much time or navigating through long menu hierarchies. 
			We designed What’s the Scene? as a location-aware iPhone application with a home screen that 
			features a 1-10 rating, as well as a text box for entering short descriptions of their 
			perceptions of a particular sub-event. After the rating is entered, the user has access to 
			view all other users' ratings that have been entered into the system within the last few hours 
			overlaid on an interactive map that can be zoomed and dragged with the iPhone’s standard 
			multi-touch interaction commands. We also included a search function to allow users to find 
			ratings for other “scenes” outside their current location.
			</p>
