			<h2>Problem Space</h2>
			<p class="text">
			The home energy monitoring device landscape has exploded in recent times. There are 
			many devices on the market, but most don't offer a fully-fledged user experience. Some 
			require a extensive technical knowledge to install, some present usage data at a 
			whole-house level. Most present energy consumption data in terms of kilowatt-hours of 
			usage, which causes a disconnect between consumption and its effect on your wallet. 
			</p>
			<h2>Solution</h2>
			<p class="text">			
			We decided to approach our solution for a whole-home energy monitoring device by 
			creating a multi-layered presentation model. Through several rounds of brainstorming 
			we ended up with a simple and easy to operate interface that revolves around a home 
			screen that is optimized for quick information retrieval. The appliances that are attached 
			to the device display as icons at various sizes depending on how much energy they are 
			consuming - the larger they are, the more energy being consumed. We also designed a 
			color-coded presentation layer that displays a color for being over the average 
			consumption and a color for being under the average consumption. The device itself 
			was designed as an internet connected, 7 inch square touch screen with an LED back-light 
			that would change color based on whole-house energy consumption. Through prototype 
			testing we discovered that the system was overall very easy to use and our test subjects 
			agreed that this device would be very useful. We never took this device past the 
			prototype phase, but my next step would be testing different data visualization 
			metaphors to see which improve information retrieval effectiveness and improve the 
			overall experience.
			</p>
